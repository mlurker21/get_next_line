#include "../includes/libft.h"

int	ft_intlen(int n)
{
	int i;

	i = 0;
	if (n >= 0)
	{
		while (n >= 10)
		{
			n /= 10;
			i++;
		}
		return (i + 1);
	}
	else
	{
		while (n <= -10)
		{
			n /= 10;
			i++;
		}
		return (i + 2);
	}
}
