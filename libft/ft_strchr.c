#include "../includes/libft.h"

char		*ft_strchr(const char *s, int c)
{
	if (s)
	{
		if (*s == c)
			return ((char *)s);
		while (*s)
		{
			if (*s == c)
				return ((char *)s);
			s++;
		}
	}
	return (NULL);
}
